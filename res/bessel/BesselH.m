function H = BesselH(nu,K,z)
if nu == 0
    U = [
        1
        -6.25000000000000000000e-02
        1.03515625000000000000e-01
        -5.42846679687500000000e-01
        5.84869956970214843750e+00
        -1.06886793971061706543e+02
        ];
    Phi = [
        -1.25000000000000000000e-01
        6.51041666666666712926e-02
        -2.09570312499999994449e-01
        1.63806588309151779370e+00
        -2.34751277499728736586e+01
        5.35640519510615945364e+02
        ];
elseif nu == 1
    U = [
        1   % 1
        1.87500000000000000000e-01  % 1/z^2
        -1.93359375000000000000e-01
        8.05297851562500000000e-01
        -7.73995399475097656250e+00
        1.32761824250221252441e+02
        ];
    Phi = [
        3.75000000000000000000e-01  % 1/z
        -1.64062500000000000000e-01 % 1/z^3
        3.70898437500000011102e-01
        -2.36939784458705338110e+00
        3.06240119934082031250e+01
        -6.59185221823778988437e+02
        ];
else
end
u = 0;
phi = z-(nu/2+1/4)*pi;
for m = 0 : 5
    u = u + U(m+1) * (1./z).^(2*m);
    phi = phi + Phi(m+1) * (1./z).^(2*m+1);
end
if K == 1
    H = sqrt(2./(pi*z)) .* u .* exp(1i*phi);
elseif K == 2
    H = sqrt(2./(pi*z)) .* u .* exp(-1i*phi);
else
end
end
